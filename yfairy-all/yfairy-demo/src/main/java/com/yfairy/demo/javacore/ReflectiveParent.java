package com.yfairy.demo.javacore;

public class ReflectiveParent {

	public String id;

	public String age;

	public String getId() {
		return id;
	}

	public void setId(String id) {
		this.id = id;
	}

	public String getAge() {
		return age;
	}

	public void setAge(String age) {
		this.age = age;
	}

}
